int	ft_count_if(char **tab, int(*f)(char*))
{
	int	i;
	int	coun;

	i = 0;
	coun = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1);
			coun++;
		i++;
	}
	return (coun);
}