#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_put_da_strr(char *str)
{
	while(*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	while (av[i])
	{
		ft_put_da_strr(av[i]);
		write(1, "\n", 1);
		i++;
	}
	
}